package com.dmdev.cs.lesson7;

public class Lesson7Runner {

    public static void main(String[] args) {
        // Литералы - это константы
        // byte short не имеет литералом
        // Строковый литерал - не является примитивным
        System.out.println(25); // Литерал int
        System.out.println(25L); // Литерал long

        System.out.println(23.879); // double вещественный литерал
        System.out.println(23.879F); // float вещественный литерал

        System.out.println('?'); // char
        System.out.println("?."); // String

        System.out.println(true); // boolean
    }
}
